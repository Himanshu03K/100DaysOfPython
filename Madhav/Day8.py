#PROGRAM TO KNOW HOW MANY CAN ARE WE GOING TO USE TO PAINT THE WALL
height=eval(input("ENTER THE HEIGHT OF WALL: "))
width=eval(input("ENTER THE WIDTH OF WALL: "))
num_of_cans=(height*width)/5
num_of_cans=round(num_of_cans)
print("THE TOTAL NO. OF CANS WE ARE GOING TO USE TO PAINT THE WALL IS",num_of_cans)